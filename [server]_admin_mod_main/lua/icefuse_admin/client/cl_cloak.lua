hook.Add('DrawPhysgunBeam', 'IcefuseAdmin.cloak', function(ply)
    if ply:GetNetVar('cloaked') == true and ply ~= LocalPlayer() then
        return false
    end
end)

hook.Add('Tick', 'IcefuseAdmin.cloak', function()
    for _, ply in ipairs(player.GetAll()) do
        if ply:GetNetVar('cloaked') == true then
            ply:SetNoDraw(true)
        end
    end
end)
